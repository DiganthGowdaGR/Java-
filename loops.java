class loops{

    public static void main(String[] args) {
        int i=1;

        //While loop
        while(i<=50){
            System.out.println("hey, how are you");
            i++; 
        }
        System.out.println(" ");

        //for loop
        for(int a=1; a<=10; a++){
            System.out.println("count:"+a);
        }
        System.out.println(" ");

        //do-while loop
        int p=1;
        do { 
            System.out.println("hello");
            p++;
        }while(p<=10);
        System.out.println(" ");
    }
}
